public class PositivoNegativo {
    public static void main(String[] args) throws Exception {
        int num1 = -5;
        
        if (num1 >= 0) {
            System.out.println(num1 + " -> Positivo");
        } 
        else {
            System.out.println(num1 + " -> Negativo");
        }
    }
}
